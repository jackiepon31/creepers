    public static int check(String string) {
        String[] arr = new String[14];
        arr = string.split("");
        int midRes = 10;
        for (int i = 0; i < 14; i++) {
            int temp = Integer.parseInt(arr[i]);
            midRes = (midRes + temp) % 10 == 0 ? 10 : (midRes + temp) % 10;
            midRes = (midRes * 2) % 11;
        }
        return (11 - midRes) == 10 ? 0 : (11 - midRes);
    }

