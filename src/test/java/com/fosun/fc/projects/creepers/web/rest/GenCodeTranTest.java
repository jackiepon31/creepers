    public static List<String> getSequenceList(int strLength) {
        List<String> resultList = new ArrayList<String>();
        int max = 1;
        for (int i = 0; i < strLength; i++) {
            max *= 10;
        }
        for (int i = 0; i < max; i++) {
            StringBuilder sb = new StringBuilder();
            sb.append(ZERO_SIX).append(i);
            sb = new StringBuilder(sb.toString().substring(sb.length() - strLength, sb.length()));
            resultList.add(sb.toString());
        }
        return resultList;
    }

